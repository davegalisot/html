                    <h1 class="color-titulo">integración urbana de sistemas de transporte, intermodalidad</h1>
                    <p>El trazado urbano de las infraestructuras de transporte requiere de una buena integración para evitar el
                        rechazo social. Tanto el ferrocarril como las carreteras de gran capacidad requieren un tratamiento de
                        borde que las haga amigables con el entorno, evitando los problemas que producen. Por otro lado, la escasez
                        de suelo en los entornos urbanos hace necesario que diversos usos compartan el disponible, siendo uno de
                        esos usos a atender el transporte, que debe garantizar la movilidad de todos los ciudadanos. La ordenación
                        del transporte consiste en buscar el difícil equilibrio entre la ocupación mínima de suelo y la accesibilidad
                        universal al sistema.</p>
                    <p>Cada modo de transporte tiene un ámbito de aplicación en el que es óptimo, por lo que un sistema de transporte
                        urbano no se puede basar en un único modo. Cuanto mayor es el área urbana que se quiere atender mayor es
                        el número de modos que existen y mayores son, por tanto, las necesidades de intercambio.</p>
